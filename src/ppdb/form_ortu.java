    private void buttonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonSimpanActionPerformed
String NIS = tfNIS.getText();
    String nama = tfNAMA.getText();
    String ayah = tfAYAH.getText();
    String kerjaAyah = tfKerjaAyah.getText();
    String gajiAyah = (String) comboAyah.getSelectedItem();
    String ibu = tfIbu.getText();
    String kerjaIbu = tfKerjaIbu.getText();
    String gajiIbu = (String) comboIbu.getSelectedItem();

    try {
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/ppdb-db", "root", "");
        String sql = "INSERT INTO formortu (NIS, nama, ayah, kerjaayah, gajiayah, ibu, kerjaibu, gajiibu) VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, NIS);
        pst.setString(2, nama);
        pst.setString(3, ayah);
        pst.setString(4, kerjaAyah);
        pst.setString(5, gajiAyah);
        pst.setString(6, ibu);
        pst.setString(7, kerjaIbu);
        pst.setString(8, gajiIbu);

        int affectedRows = pst.executeUpdate();
        if (affectedRows > 0) {
            JOptionPane.showMessageDialog(this, "Data orang tua berhasil disimpan ke database");
            tampilkan(); // Refresh tabel setelah penyimpanan berhasil
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data orang tua ke database");
        }

        pst.close();
        cn.close();
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "Koneksi ke database gagal: " + ex.getMessage());
        ex.printStackTrace();
    }
    }//GEN-LAST:event_buttonSimpanActionPerformed
    
