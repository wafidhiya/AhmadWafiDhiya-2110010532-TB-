    String nama = tfNAMA.getText();
    String ayah = tfAYAH.getText();
    String kerjaAyah = tfKerjaAyah.getText();
    String gajiAyah = (String) comboAyah.getSelectedItem();
    String ibu = tfIbu.getText();
    String kerjaIbu = tfKerjaIbu.getText();
    String gajiIbu = (String) comboIbu.getSelectedItem();

